/* Whats is Debouncing and Throttling */

/* Both are programming concepts which are used to limit the rate at which a function is executed
particularly in response to events like user input, scrolling, window resizing etc. */


/* what is the difference between both */

/* Throttling limits the rate at which a function is called, ensuring it executes at intervals */

/* Debouncing delays the execution of a function until a period of inactivity has passed */